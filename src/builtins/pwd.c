#include "../../inc/minishell.h"

int	ft_pwd(void)
{
	char	pwd[1024];

	if (getcwd(pwd, 1024))
	{
		printf("%s\n", pwd);
		return (0);
	}
	perror("get cwd()");
	return (1);
}
